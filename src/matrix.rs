#[inline]
pub fn transpose8x8_u64(n: u64) -> u64 {
    let m = (n ^ (n >> 7)) & 0x00aa00aa00aa00aa;
    let n = n ^ m ^ (m << 7);
    let m = (n ^ (n >> 14)) & 0x0000cccc0000cccc;
    let n = n ^ m ^ (m << 14);
    let m = (n ^ (n >> 28)) & 0x00000000f0f0f0f0;

    n ^ m ^ (m << 28)
}

pub fn transpose8x8(mut a: [u8; 8]) -> [u8; 8] {
    let mut j: usize = 4;
    let mut k;
    let mut m: u8 = 0x0F;
    let mut t: u8;
    while j != 0 {
        k = 0;
        while k < 8 {
            t = (a[k] ^ (a[k | j] >> j)) & m;
            a[k] ^= t;
            a[k | j] ^= t << j;
            k = ((k | j) + 1) & !j;
        }
        j >>= 1;
        m ^= m << j;
    }
    a
}

